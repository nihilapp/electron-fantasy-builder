/**
 * 아이콘 이름 리터럴 유니온 타입 코드젠
 * @iconify-json/* 패키지 루트(icons export)를 읽어 IconName 타입을 생성한다.
 * 실행: node scripts/generate-icon-name-type.mjs
 */

import { writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const require = createRequire(import.meta.url);
const __dirname = dirname(fileURLToPath(import.meta.url));

const PACKAGES = [
  '@iconify-json/bx',
  '@iconify-json/carbon',
  '@iconify-json/fa6-regular',
  '@iconify-json/fa6-solid',
  '@iconify-json/game-icons',
  '@iconify-json/heroicons',
  '@iconify-json/lucide',
  '@iconify-json/mdi',
  '@iconify-json/ph',
  '@iconify-json/ri',
  '@iconify-json/tabler',
];

const allNames = [];

for (const pkg of PACKAGES) {
  try {
    const pkgData = require(pkg);
    const data = pkgData.icons ?? pkgData;
    const prefix = data.prefix ?? pkgData.prefix ?? pkg.replace('@iconify-json/', '');
    const icons = data.icons ?? {};
    for (const key of Object.keys(icons)) {
      allNames.push(`${prefix}:${key}`);
    }
  }
  catch (err) {
    console.error(`Failed to load ${pkg}:`, err.message);
  }
}

allNames.sort();

const lines = [
  '// AUTO-GENERATED by scripts/generate-icon-name-type.mjs — do not edit',
  '',
  '/** 모든 아이콘 세트의 prefix:name 리터럴 유니온 */',
  'export type IconName =',
  ...allNames.map((name) => `  | ${JSON.stringify(name)}`),
  ';',
  '',
];

const outPath = join(__dirname, '..', 'src', 'renderer', 'data', 'icon-name.generated.ts');
writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log(`Wrote ${outPath} (${allNames.length} icon names)`);
